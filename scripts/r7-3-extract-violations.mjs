// scripts/r7-3-extract-violations.mjs
//
// Read the latest Lighthouse JSON reports under docs/gate-a-evidence/
// and emit a per-page bucket of failing accessibility audits, with the
// per-item DOM selector + node label for each. Output is markdown that
// the R7.3 memo embeds directly.
//
// Run: node scripts/r7-3-extract-violations.mjs > docs/gate-a-evidence/r7-3-violation-trace.md

import { readFileSync, existsSync } from "node:fs";
import { resolve } from "node:path";

const REPORTS = [
  ["/", "docs/gate-a-evidence/lh-landing-mobile.report.json"],
  ["/login", "docs/gate-a-evidence/lh-login-mobile.report.json"],
  ["/programs", "docs/gate-a-evidence/lh-programs-mobile.report.json"],
];

function summarize(label, path) {
  if (!existsSync(path)) {
    return `\n## ${label}\n\n(missing report: ${path})\n`;
  }
  const j = JSON.parse(readFileSync(path, "utf8"));
  const a11y = j.categories?.accessibility;
  if (!a11y) return `\n## ${label}\n\n(no accessibility category)\n`;
  const score = a11y.score != null ? Math.round(a11y.score * 100) : "?";
  const refs = a11y.auditRefs || [];

  // Collect failing audits (score < 1 and applicable).
  const fails = [];
  for (const ref of refs) {
    const audit = j.audits?.[ref.id];
    if (!audit) continue;
    if (audit.scoreDisplayMode === "notApplicable") continue;
    if (audit.scoreDisplayMode === "manual") continue;
    if (audit.score === 1) continue;
    // score === null OR < 1 with applicable mode = failed/needs review
    fails.push({ id: ref.id, title: audit.title, description: audit.description, details: audit.details });
  }

  let out = `\n## ${label} — a11y score ${score}/100, ${fails.length} failing audit(s)\n`;
  if (fails.length === 0) {
    out += `\n(All a11y audits pass.)\n`;
    return out;
  }
  for (const f of fails) {
    out += `\n### ${f.id}\n`;
    out += `**Title:** ${f.title}\n\n`;
    if (f.description) out += `**Why:** ${f.description.split("\n")[0]}\n\n`;
    const items = f.details?.items || [];
    if (items.length === 0) {
      out += `_(No item-level node detail in the report.)_\n`;
      continue;
    }
    out += `**Failing nodes (${items.length}):**\n\n`;
    for (let i = 0; i < Math.min(items.length, 6); i++) {
      const it = items[i];
      const node = it.node || {};
      const selector = node.selector || node.path || "(no selector)";
      const snippet = (node.snippet || "").replace(/\s+/g, " ").slice(0, 160);
      const explanation = node.explanation || "";
      out += `${i + 1}. \`${selector}\`\n`;
      if (snippet) out += `   - snippet: \`${snippet}\`\n`;
      if (explanation) out += `   - explanation: ${explanation.slice(0, 240)}\n`;
    }
    if (items.length > 6) out += `\n_(...${items.length - 6} more nodes omitted for brevity)_\n`;
  }
  return out;
}

let md = `# R7.3 — Lighthouse a11y violation trace\n\n`;
md += `Extracted from the latest Lighthouse mobile reports in docs/gate-a-evidence/.\n`;
md += `Generated by scripts/r7-3-extract-violations.mjs on ${new Date().toISOString()}.\n`;
for (const [label, path] of REPORTS) {
  md += summarize(label, resolve(process.cwd(), path));
}
process.stdout.write(md);
